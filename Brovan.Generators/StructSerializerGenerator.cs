        private static void ReportUnsupported(GenContext gc, IFieldSymbol field)
        {
            var loc = field.Locations.FirstOrDefault() ?? Location.None;
            gc.Ctx.ReportDiagnostic(Diagnostic.Create(UnsupportedFieldDiagnostic, loc, field.Name, field.ContainingType?.Name ?? "?"));
        }

        private static void ReportNestingTooDeep(GenContext gc, INamedTypeSymbol structType)
        {
            var loc = structType.Locations.FirstOrDefault() ?? Location.None;
            gc.Ctx.ReportDiagnostic(Diagnostic.Create(NestingTooDeepDiagnostic, loc, structType.Name, MaxNestingDepth));
        }

        // --- Layout / size helpers ---

        private static bool IsExplicitLayout(INamedTypeSymbol sym)
        {
            // TypedConstant.Value for an enum-typed constructor argument is the boxed underlying
            // integral value (e.g. 2), not the LayoutKind member itself -- compare against the int.
            return sym.GetAttributes().Any(a =>
                a.AttributeClass?.ToDisplayString() == "System.Runtime.InteropServices.StructLayoutAttribute" &&
                a.ConstructorArguments.Length > 0 &&
                a.ConstructorArguments[0].Value is int layoutKindValue &&
                layoutKindValue == (int)LayoutKind.Explicit);
        }

        private static int GetDeclaredStructLayoutSize(INamedTypeSymbol sym)
        {
            var attr = sym.GetAttributes().FirstOrDefault(a =>
                a.AttributeClass?.ToDisplayString() == "System.Runtime.InteropServices.StructLayoutAttribute");
            if (attr == null) return 0;

            foreach (var na in attr.NamedArguments)
                if (na.Key == "Size" && na.Value.Value is int sizeVal)
                    return sizeVal;

            return 0;
        }

        private static IEnumerable<IFieldSymbol> GetOrderedFields(INamedTypeSymbol sym, bool isExplicit)
        {
            var fields = sym.GetMembers().OfType<IFieldSymbol>().Where(f => !f.IsStatic).ToList();
            if (isExplicit)
                fields = fields.OrderBy(GetExplicitFieldOffset).ToList();
            return fields;
        }

        private static int GetExplicitFieldOffset(IFieldSymbol field)
        {
            var attr = field.GetAttributes().FirstOrDefault(a => a.AttributeClass?.Name == "FieldOffsetAttribute");
            return attr != null ? (int)attr.ConstructorArguments[0].Value! : 0;
        }

        private static (int inlineSize, bool ascii, object? ptrContent) GetFieldAttrs(IFieldSymbol field, GenContext gc)
        {
            int inlineSize = 0;
            bool ascii = false;
            object? ptrContent = null;

            foreach (var attr in field.GetAttributes())
            {
                if (SymbolEquals(attr.AttributeClass, gc.InlineAttr) && attr.ConstructorArguments.Length > 0)
                {
                    inlineSize = (int)attr.ConstructorArguments[0].Value!;
                    foreach (var na in attr.NamedArguments)
                        if (na.Key == "Ascii" && na.Value.Value is bool b)
                            ascii = b;
                }
                if (SymbolEquals(attr.AttributeClass, gc.PtrAttr) && attr.ConstructorArguments.Length > 0)
                {
                    ptrContent = attr.ConstructorArguments[0].Value;
                }
            }

            return (inlineSize, ascii, ptrContent);
        }

        private static int ComputeSize(INamedTypeSymbol sym, bool is64, GenContext gc)
        {
            bool isExplicit = IsExplicitLayout(sym);
            int max = 0;
            int runningTotal = 0;

            foreach (var field in GetOrderedFields(sym, isExplicit))
            {
                int sz = GetFieldSize(field, is64, gc);
                if (isExplicit)
                    max = Math.Max(max, GetExplicitFieldOffset(field) + sz);
                else
                    runningTotal += sz;
            }

            int computed = isExplicit ? max : runningTotal;
            int declared = GetDeclaredStructLayoutSize(sym);
            return Math.Max(computed, declared);
        }

        private static int GetFieldSize(IFieldSymbol field, bool is64, GenContext gc)
        {
            var (inlineSize, _, _) = GetFieldAttrs(field, gc);
            var ft = field.Type;

            if (ft is IArrayTypeSymbol arrType)
            {
                if (arrType.ElementType.SpecialType == SpecialType.System_Byte)
                    return inlineSize > 0 ? inlineSize : (is64 ? 8 : 4);

                if (inlineSize <= 0)
                    return is64 ? 8 : 4;

                return GetElementTypeSize(arrType.ElementType, is64, gc) * inlineSize;
            }

            if (ft.TypeKind == TypeKind.Enum)
                ft = ((INamedTypeSymbol)ft).EnumUnderlyingType!;

            switch (ft.SpecialType)
            {
                case SpecialType.System_Boolean: return 1;
                case SpecialType.System_Byte: return 1;
                case SpecialType.System_SByte: return 1;
                case SpecialType.System_Int16: return 2;
                case SpecialType.System_UInt16: return 2;
                case SpecialType.System_Int32: return 4;
                case SpecialType.System_UInt32: return 4;
                case SpecialType.System_Int64: return 8;
                case SpecialType.System_UInt64: return 8;
                case SpecialType.System_Single: return 4;
                case SpecialType.System_Double: return 8;
                case SpecialType.System_String: return inlineSize > 0 ? inlineSize : (is64 ? 8 : 4);
            }

            if (ft.IsValueType && !IsPrimitiveType(ft))
                return ComputeSize((INamedTypeSymbol)ft, is64, gc);

            return 0;
        }

        private static int GetElementTypeSize(ITypeSymbol elemType, bool is64, GenContext gc)
        {
            var ft = elemType;
            if (ft.TypeKind == TypeKind.Enum)
                ft = ((INamedTypeSymbol)ft).EnumUnderlyingType!;

            switch (ft.SpecialType)
            {
                case SpecialType.System_Boolean: return 1;
                case SpecialType.System_Byte: return 1;
                case SpecialType.System_SByte: return 1;
                case SpecialType.System_Int16: return 2;
                case SpecialType.System_UInt16: return 2;
                case SpecialType.System_Int32: return 4;
                case SpecialType.System_UInt32: return 4;
                case SpecialType.System_Int64: return 8;
                case SpecialType.System_UInt64: return 8;
                case SpecialType.System_Single: return 4;
                case SpecialType.System_Double: return 8;
            }

            if (ft.IsValueType && !IsPrimitiveType(ft))
                return ComputeSize((INamedTypeSymbol)ft, is64, gc);

            return 0;
        }

        private static bool IsNumericPrimitive(ITypeSymbol t)
        {
            switch (t.SpecialType)
            {
                case SpecialType.System_Byte:
                case SpecialType.System_SByte:
                case SpecialType.System_Int16:
                case SpecialType.System_UInt16:
                case SpecialType.System_Int32:
                case SpecialType.System_UInt32:
                case SpecialType.System_Int64:
                case SpecialType.System_UInt64:
                case SpecialType.System_Single:
                case SpecialType.System_Double:
                    return true;
                default:
                    return false;
            }
        }

        private static bool IsPrimitiveType(ITypeSymbol t)
        {
            switch (t.SpecialType)
            {
                case SpecialType.System_Boolean:
                case SpecialType.System_Byte:
                case SpecialType.System_SByte:
                case SpecialType.System_Int16:
                case SpecialType.System_UInt16:
                case SpecialType.System_Int32:
                case SpecialType.System_UInt32:
                case SpecialType.System_Int64:
                case SpecialType.System_UInt64:
                case SpecialType.System_Single:
                case SpecialType.System_Double:
                    return true;
                default:
                    return false;
            }
        }

        private static bool SymbolEquals(ISymbol? a, ISymbol? b)
        {
            if (a == null || b == null) return false;
            return SymbolEqualityComparer.Default.Equals(a, b);
        }
